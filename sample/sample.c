void pceAppInit(void){
  int result;

  pceLCDDispStop();
  pceLCDSetBuffer(vbuff);
  memset(vbuff, 0, DISP_X * DISP_Y);
  pceLCDDispStart();

  pceCPUSetSpeed(CPU_SPEED_NORMAL);
  pceAppSetProcPeriod(100);

  result = loadInst();
  InitMusic();
  PlayMusic(music);

  pceFontSetType(0x80);
  pceFontSetPos(14, 9);
  pceFontPrintf("ドラム音色分離キット");
  pceFontSetPos(19, 19);
  pceFontPrintf("サンプルプログラム");
  pceFontSetPos(19, 39);
  pceFontPrintf("ドラム音色読み込み");
  pceFontSetPos(54, 49);
  pceFontPrintf("%s", (result == 0) ? "成功" : "失敗");
  pceFontSetPos(14, 69);
  pceFontPrintf("セレクトボタンで終了");
  pceLCDTrans();
}

void pceAppProc(int cnt){

  // セレクトボタンで終了
  if(pcePadGet() & TRG_D){
    pceAppReqExit(0);
  }
}

void pceAppExit(void){
  StopMusic();
}
